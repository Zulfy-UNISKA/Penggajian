    public void tampilDataKaryawan() {
        DefaultTableModel karyawanModel = new DefaultTableModel();
        karyawanModel.addColumn("Nama Karyawan");
        karyawanModel.addColumn("Status");
        karyawanModel.addColumn("Golongan");
        karyawanModel.addColumn("Masa Kerja");
        
        // Masih Show Column (Header), sekarang output kan ke JTable
        for (int i = 0; i < employee.listDataNama().size(); i++) {
            String nama     = employee.listDataNama().get(i);
            String golongan = employee.listDataGolongan().get(i);
            String status   = employee.listDataStatus().get(i);
            String masa     = employee.listDataMasaKerja().get(i).toString();
            
            Object[] barisData = {nama, status, golongan, masa};
            karyawanModel.addRow(barisData);
        }
        
        tblKaryawan.setModel(karyawanModel);
    }

