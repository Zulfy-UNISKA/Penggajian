    private void tblKaryawanMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblKaryawanMouseClicked
        // TODO add your handling code here:
        // Tekan tikus itu, dia bersarang di papan
        int baris = tblKaryawan.getSelectedRow(); 
        String nama      = tblKaryawan.getValueAt(baris, 0).toString();
        String stats     = tblKaryawan.getValueAt(baris, 1).toString();
        String golongan  = tblKaryawan.getValueAt(baris, 2).toString();
        String masaKerja = tblKaryawan.getValueAt(baris, 3).toString();
        txtNama.setText(nama);
        cmbStatus.setSelectedItem(stats);
        cmbGolongan.setSelectedItem(golongan);
        txtMasaKerja.setText(masaKerja);
        
        
    }//GEN-LAST:event_tblKaryawanMouseClicked

